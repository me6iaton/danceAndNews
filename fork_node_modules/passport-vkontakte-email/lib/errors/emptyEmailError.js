/**
 * `BadRequestError` error.
 *
 * @api public
 */
function emptyEmailError(message) {
  Error.call(this);
  Error.captureStackTrace(this, arguments.callee);
  this.name = 'emptyEmailError';
  this.message = message || null;
};

/**
 * Inherit from `Error`.
 */
emptyEmailError.prototype.__proto__ = Error.prototype;


/**
 * Expose `BadRequestError`.
 */
module.exports = emptyEmailError;
